# Given a binary tree, determine if it is height-balanced.
# 
# For this problem, a height-balanced binary tree is defined as:
# 
# 
# a binary tree in which the depth of the two subtrees of every node never
# differ by more than 1.
# 
# 
# Example 1:
# 
# Given the following tree [3,9,20,null,null,15,7]:
# 
# 
# ⁠   3
# ⁠  / \
# ⁠ 9  20
# ⁠   /  \
# ⁠  15   7
# 
# Return true.
# 
# Example 2:
# 
# Given the following tree [1,2,2,3,3,null,null,4,4]:
# 
# 
# ⁠      1
# ⁠     / \
# ⁠    2   2
# ⁠   / \
# ⁠  3   3
# ⁠ / \
# ⁠4   4
# 
# 
# Return false.
# 
#
# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def isBalanced(self, root: TreeNode) -> bool:
        is_balanced, depth = self.isBalancedHelper(root)
        return is_balanced
    
    def isBalancedHelper(self, root):
        if root is None:
            return (True, 0)

        is_left, left_depth = self.isBalancedHelper(root.left)
        is_right, right_depth = self.isBalancedHelper(root.right)
        
        if is_left and is_right and abs(left_depth - right_depth) <= 1:
            return (True, max(left_depth + 1, right_depth + 1))
        else:
            return (False, 0)

